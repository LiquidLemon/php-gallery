<h2>cd ctf/</h2>
<p>
  <abbr title="Capture The Flag">CTF</abbr>s are a type of hacking competitions in which
participants try to find and exploit vulnerabilities in a number of provided
applications. There are two main types of those events:
</p>
<figure>
  <dl>
    <dt>Attack-defense</dt>
    <dd>
      <p>
        In this type of competition each team is given a virtual machine running
        several services. The main goal is to reverse engineer the applications and
        secure them while breaking into the instances hosted by other participants
        and keeping your own services operational.
      </p>
      <p>
        This type of event is much rarer because of the necessary infrastructure.
        It also tends to be much harder and is rarely available online.
      </p>
    </dd>
    <dt>Jeopardy</dt>
    <dd>
      <p>
        This type of event is much more casual and is suitable for solitary
        participants. Not unlike the other type, it tasks the players with
        finding flaws in several miscellaneous applications. Solving a challenge
        is rewarded with a flag: a string of characters in some specified
        format. The flag can then be submitted to a central scoreboard to mark
        the challenge as solved.
      </p>
      <p>
        These events vary heavily in both the difficulty of the challenges and
        their timespan: from as little as 24 hours to multiple weeks.
      </p>
    </dd>
  </dl>
</figure>
